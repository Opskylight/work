未料，這還沒完，鄒女發現李男壓根沒打算停車，甚至還加速前行，鄒女口氣有些著急地問：「你要幹嘛」，李男則害怕地說：「對不起啦，我不是故意的，對不起好不好，我不是故意的」，並要鄒女不要再叫了，指稱讓他借車直到抵達他阿姨家就好，到時候車子會立刻還鄒女。

鄒女聽完一席話後，便火大的怒吼：「我都給你錢了，你自己坐車去，你！現！在！給！我！下！車！」李男被吼了之後，害怕且情緒不穩定的開著車在街上亂竄，甚至差點擦撞到機車騎士，蛇行在道路上，還帶著哭腔的說：「我要怎麼下車，我下車你會叫人抓我」、「我怕你會叫人抓我」等。

最後鄒女崩潰的回：「我幹嘛叫人抓你，我有事要辦，我買個飯被你搶車，你現在給我下車」，李男見事已至此，便乖乖地下車，狼狽地逃跑。事後鄒女趕緊報警抓人，新興分局也火速在16分鐘內逮人，警詢後依搶奪罪嫌移送法辦。</p><br>
